// 1. Print numbers frim 1 to 10 using for loop.

for (let i=1; i<=10; i++){
    console.log(i);
}

// 2. Print numbers from 10 to 1 using while loop.

let j=10;
while (j >= 1){
    console.log(j);
    j--;
}

// 3. Print even numbers from 1 to 20 using for loop.
for ( let n=1; n<=20; n++){
    if (n % 2 === 0){
        console.log(n);
    }
}
