// And operator example(&& )
let x=10;
let y=20;

if(x> 5 && y < 25){
    console.log("A");
}
else{
    console.log("B");
}

// Or operator example (||)
let isAdmin = true;
let isLoggedIn = false;

if(isAdmin || isLoggedIn){
    console.log("Access granted");
}
else{
    console.log("Access denied");
}